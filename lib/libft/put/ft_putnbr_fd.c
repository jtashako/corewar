#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	(n < 0) ? ft_putchar_fd('-', fd) : 0;
	n *= (n > 0 ? -1 : 1);
	(n <= -10) ? ft_putnbr_fd(-(n / 10), fd) : 0;
	ft_putchar_fd('0' - n % 10, fd);
}
