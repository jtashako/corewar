#include "asm.h"

int	valid_number(char *cmd)
{
	if (*cmd == '-' || *cmd == '+')
		cmd++;
	while (*cmd)
	{
		if (*cmd < '0' || *cmd > '9')
			return (0);
		cmd++;
	}
	return (1);
}
